
import java.util.Scanner;

public class Ex19 {
    public static void main(String[] args) {
        Scanner keyboard = new Scanner(System.in);
        int n = keyboard.nextInt();
        int s = 0;
        int i = 0;
        while(i <= n){
            if(i % 2 == 0);
                s += i;
                i += 1;
            
        }
        System.out.print(s);
    }
}
